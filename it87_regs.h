enum it87_const {
	IT87_BASE_PORT	= 0x2E,
	IT87_BASE_DATA	= 0x2F,

	IT87_ADDR_PORT_OFFSET	= 0x05,
	IT87_DATA_PORT_OFFSET	= 0x06,

	IT87_WAIT	= 1600,	// wait this many Âµs for the device to become ready.
	IT87_BUSY	= 0x80,
	IT87_FANDIV	= 0x09,	// Div by two = 00 001-001
};


// Global Registers of the IT87xx
enum it87_global_registers {
	IT87_CONFIG_CTRL = 0x02,
	IT87_LDN = 0x07,
	IT87_CHIP_ID_1 = 0x20,
	IT87_CHIP_ID_2 = 0x20,
	IT87_CONFIG_SELECT_CHIP_VER = 0x22,
};

